package user;

public class sizeinformation {
	private String userID_size;
	private int tall;
	private int Weight;
	private int bustsize;
	private int length;
	
	public String getUserID_size() {
		return userID_size;
	}

	public void setUserID_size(String userID_size) {
		this.userID_size = userID_size;
	}

	public int getTall() {
		return tall;
	}

	public void setTall(int tall) {
		this.tall = tall;
	}

	public int getWeight() {
		return Weight;
	}

	public void setWeight(int weight) {
		Weight = weight;
	}

	public int getBustsize() {
		return bustsize;
	}

	public void setBustsize(int bustsize) {
		this.bustsize = bustsize;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}


}
